use num_enum::IntoPrimitive;
#[repr(u8)]
#[derive(Debug, Copy, Clone, Eq, PartialEq, IntoPrimitive)]
pub enum Registers {
    TouchStatusL = 0x00,
    TouchStatusH = 0x01,
    FiltData0L = 0x04,
    FiltData0H = 0x05,
    Baseline0 = 0x1E,
    Mhdr = 0x2B,
    Nhdr = 0x2C,
    Nclr = 0x2D,
    Fdlr = 0x2E,
    Mhdf = 0x2F,
    Nhdf = 0x30,
    Nclf = 0x31,
    Fdlf = 0x32,
    Nhdt = 0x33,
    Nclt = 0x34,
    Fdlt = 0x35,
    TouchTh0 = 0x41,
    ReleaseTh0 = 0x42,
    Debounce = 0x5B,
    Config1 = 0x5C,
    Config2 = 0x5D,
    ChargeCurr0 = 0x5F,
    ChargeTime1 = 0x6C,
    Ecr = 0x5E,
    AutoConfig0 = 0x7B,
    AutoConfig1 = 0x7C,
    UpLimit = 0x7D,
    LowLimit = 0x7E,
    TargetLimit = 0x7F,
    GpioDir = 0x76,
    GpioEn = 0x77,
    GpioSet = 0x78,
    GpioClr = 0x79,
    GpioToggle = 0x7A,
    SoftReset = 0x80,
}
